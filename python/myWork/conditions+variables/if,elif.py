v = int(input('Введи, сколько тебе лет: '))
if v < 18:
    print('Привет, юный хацкер')
elif v < 30:
    print('Превед, олдскул')
elif v < 65:
    print('Решил пересесть с ассемблера на Python?')
elif v < 100:
    print('На пенсии — самое время покодить')
elif v < 100000:
    print('Клан бессмертных приветствует тебя!')